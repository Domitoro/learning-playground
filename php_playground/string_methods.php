<?php
$str_var = "Here I go. I am a String. I am a powerful entity!\n" ; // declare a variable

// strtolower — Make a string lowercase
strtolower($str_var); // here i go. i am a string. i am a powerful entity!

// strtoupper — Make a string uppercase
echo strtoupper($str_var); // HERE I GO. I AM A STRING. I AM A POWERFUL ENTITY!
?>